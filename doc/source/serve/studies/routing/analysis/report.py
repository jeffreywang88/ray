def compute_algorithm_summary(df: pd.DataFrame, algorithm: str) -> Dict:
    """Compute summary statistics for an algorithm."""
    alg_df = df[df["algorithm"] == algorithm]

    return {
        "algorithm": algorithm,
        "runs": len(alg_df),
        "latency_p50_mean": alg_df["latency_p50"].mean(),
        "latency_p99_mean": alg_df["latency_p99"].mean(),
        "latency_p99_std": alg_df["latency_p99"].std(),
        "child_jains_mean": alg_df["child_jains"].mean(),
        "child_jains_std": alg_df["child_jains"].std(),
        "goodput_mean": alg_df["goodput"].mean(),
        "error_rate_mean": alg_df["error_rate"].mean(),
    }


def format_latency_table(df: pd.DataFrame) -> str:
    """Format latency statistics as markdown table."""
    rows = []
    rows.append("| Algorithm | p50 (ms) | p90 (ms) | p95 (ms) | p99 (ms) | Max (ms) |")
    rows.append("|-----------|----------|----------|----------|----------|----------|")

    for alg in df["algorithm"].unique():
        alg_df = df[df["algorithm"] == alg]
        rows.append(
            f"| {alg.replace('_', ' ').title()} | "
            f"{alg_df['latency_p50'].mean():.1f} | "
            f"{alg_df['latency_p90'].mean():.1f} | "
            f"{alg_df['latency_p95'].mean():.1f} | "
            f"{alg_df['latency_p99'].mean():.1f} | "
            f"{alg_df['latency_max'].mean():.1f} |"
        )

    return "\n".join(rows)


